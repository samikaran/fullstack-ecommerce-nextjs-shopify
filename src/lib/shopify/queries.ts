import { gql } from "graphql-request";

// Fetches cart details by ID
// Used to retrieve current cart state including items, quantities, and costs
// Required param: cartId - The unique identifier for the cart
export const GET_CART = `
  query getCart($cartId: ID!) {
    cart(id: $cartId) {
      id
      checkoutUrl        # URL for redirecting to Shopify checkout
      totalQuantity      # Total number of items in cart
      cost {
        subtotalAmount {  # Price before tax/shipping
          amount
          currencyCode
        }
        totalAmount {     # Final price including all fees
          amount
          currencyCode
        }
      }
      lines(first: 100) {  # Cart items, limited to 100 for performance
        edges {
          node {
            id
            quantity
            merchandise {
              ... on ProductVariant {
                id
                title
                price {
                  amount
                  currencyCode
                }
                product {
                  title
                  images(first: 1) {  # Only fetch first image for performance
                    edges {
                      node {
                        url
                        altText
                      }
                    }
                  }
                }
              }
            }
          }
        }
      }
    }
  }
`;

